                                <b>1. Tower 4 kaki/ Rectangular Tower</b><br>
								Sesuai dengan namanya, tower ini berbentuk segi empat dan memiliki 4 kaki. Karena konstruksinya yang kokoh tower ini diharapkan memiliki kekuatan yang optimal untuk menghindari kemungkinan roboh. Tingginya kurang lebih 42 meter serta mampu mencakup banyak antena dan radio. Tipe tower ini biasanya digunakan oleh perusahaan telekomunikasi terkemuka seperti Telkom, Indosat, XL, dll.
								<hr>
								<b>2. Tower 3 kaki/ Triangle Tower</b><br>
								Menara segitiga ini terdiri dari 3 pondasi tower. Tiap pondasi disusun dalam beberapa potongan yang berkisar 4-5 meter. Untuk menghindari hal-hal yang tidak diinginkan, misal roboh sebaiknya tower ini memakai besi yang berdiameter diatas 2 centimeter. Rata-rata tower jenis tingginya berkisar antara 40 meter dan maksimal 60 meter. Makin pendek tower tingkat keamanannya lebih tinggi. 
								<hr>
								<b>3. Tower 1 kaki/ Pole</b><br>
									Sebenarnya tower jenis ini tidak direkomendasikan karena banyak kekurangannya. Dalam penerimaan sinyal tergolong tidak stabil, mudah goyang, dan mengganggu sistem koneksi data yang berakibat pencarian di komputer terjadi secara terus-terusan. Tower ini ada 2 macam, yang pertama dibuat dengan pipa/plat baja tanpa spanner dengan diameter 40 cm hingga 50 cm dan rata-rata tingginya 42 meter. Kedua, tower yang dibuat dengan spanner yang menurut ahli pembuatannya tidak melebihi 20 meter.
